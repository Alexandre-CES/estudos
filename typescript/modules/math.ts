
export const somar = (x:number, y:number):number =>{
    return x + y
}

export const subtrair = (x:number, y:number):number =>{
    return x - y
}

export const multiplicar = (x:number, y:number):number =>{
    return x * y
}

export const dividir = (x:number, y:number):number =>{
    return x / y
}